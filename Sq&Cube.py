
# Take a number as input and find out its 
# (i) Square 
# (ii) Square Root 
# (iii) Cube 
# (iv) Cube root

n = int(input('\nEnter a number : '))

print('\nSquare of', n, 'is \t\t-->\t', n*n)
print('Square Root of', n, 'is \t-->\t', n**(1/2))
print('Cube of', n, 'is \t\t-->\t', n*n*n)
print('Cube root of', n, 'is \t-->\t', n**(1/3), '\n')